//O que é Programação Orientada a Objetos?
//
//        A POO é uma abordagem que nos permite criar programas mais estruturados, flexíveis e reutilizáveis.
//
//        Principais conceitos da POO:
//
//        -Classe: É a definição do objeto, que contém sua estrutura e comportamentos. Ela serve como um molde para a criação de objetos.
//
//        -Objeto: É uma instância de uma classe. Cada objeto possui características próprias e pode executar os comportamentos definidos na classe.
//
//        -Encapsulamento: É o princípio de esconder detalhes internos de uma classe e expor apenas o necessário para o uso externo. Isso promove a segurança e facilita a manutenção do código.
//
//        -Herança: É a capacidade de uma classe herdar características e comportamentos de outra classe. Isso permite criar hierarquias e reaproveitar código.
//
//        -Polimorfismo: É a capacidade de objetos de diferentes classes responderem de forma distinta a uma mesma mensagem. Isso aumenta a flexibilidade e modularidade do código.
//
//        Benefícios da POO:
//
//        -Reutilização de código: Com a possibilidade de herdar características e comportamentos, é possível reutilizar o código existente em diferentes partes do programa, economizando tempo e esforço.
//
//        -Modularidade: A POO incentiva a divisão do programa em módulos independentes (classes), o que facilita a manutenção e o entendimento do código.
//
//        -Facilidade de manutenção: Através do encapsulamento, é possível isolar partes do código, tornando mais fácil fazer alterações sem afetar outras partes do programa.
//
//        -Modelagem do mundo real: A POO permite criar representações mais fiéis de objetos e entidades do mundo real, facilitando a compreensão e o desenvolvimento de soluções para problemas complexos.